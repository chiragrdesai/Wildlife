    is a home to hundred of species of wild animals, reptiles, and thousands of
    birds. The diverse climate and landscape make it an ideal place for the
    great Indian wild animals. The rivers and the dense forests of the India add
    to the ecosystem, encouraging further growth of animals in India. <br>
     <br>
     Explore the treasures of Indian wild animals from India their habitat,
    habits and other interesting facts. India is the abode of marvel of nature
    called the royal Bengal tiger, great Indian elephant, Indian lion and
    gigantic Indian one horned rhino. In India tourists can embark upon elephant
    safaris and enjoy some close encounters with the wild animals of India. With
    such diversity and grandeur Indian wildlife simply defies any narration. <br>
     <br>
     The thrill of being in an Indian wildlife park is simply tempting. Close
    encounters with such magnificent wild animals in India are lifetime
    experiences, which engraves in the reminiscence of every visitor. <br>
